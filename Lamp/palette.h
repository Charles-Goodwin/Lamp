// we can incorporate a hue shift
CRGBPalette256 Rainbow_pal() {
  CRGBPalette256 palette;
  uint8_t pal[1024];
  CRGB col; 

  for (int i = 0; i<256; i+=4){
    col = CHSV(i + g_hueShift, 255, 255);
    pal[i] = i;
    pal[i+1] = col.r;
    pal[i+2] = col.g;
    pal[i+3] = col.b; 
  }
    
  palette.loadDynamicGradientPalette(pal) ;
  return palette;
} 

